/**
 * Stream orchestrator output as a single ReadableStream of tagged
 * events. Consumers (the SSE route) can call ``reader.read()`` until
 * EOF; each chunk is a single JSON object line.
 */
export function runHermes(req: HermesRequest): ReadableStream<Uint8Array> {
  const encoder = new TextEncoder();

  return new ReadableStream({
    async start(controller) {
      const write = (evt: TaggedEvent) => {
        controller.enqueue(encoder.encode(JSON.stringify(evt) + "\n"));
      };

      let tools: ToolSpec[] = [];
      try {
        tools = await listTools({ signal: req.signal });
      } catch (e) {
        // Tools are optional — providers can still answer without MCP
        // if the bridge is down.
        write({
          providerId: "anthropic",
          type: "status",
          level: "warn",
          message: `mcp bridge unavailable: ${errMsg(e)}`,
        });
      }

      const systemPrefix: ChatMessage = {
        role: "system",
        content: SYSTEM_GROUNDING,
      };
      const baseMessages = [systemPrefix, ...req.messages];

      try {
        switch (req.mode) {
          case "solo":
            await runSolo(req, baseMessages, tools, write);
            break;
          case "parallel":
            await runParallel(req, baseMessages, tools, write);
            break;
          case "synthesis":
            await runSynthesis(req, baseMessages, tools, write);
            break;
          case "relay":
            await runRelay(req, baseMessages, tools, write);
            break;
          default:
            write({
              providerId: req.providers[0]?.id ?? "anthropic",
              type: "error",
              message: `unknown mode: ${req.mode}`,
            });
        }
      } catch (e) {
        write({
          providerId: req.providers[0]?.id ?? "anthropic",
          type: "error",
          message: errMsg(e),
        });
      } finally {
        controller.close();
      }
    },
  });
}

// ── Mode implementations ─────────────────────────────────────────────
async function runSolo(
  req: HermesRequest,
  messages: ChatMessage[],
  tools: ToolSpec[],
  write: (e: TaggedEvent) => void,
): Promise<void> {
  const target = req.providers[0];
  if (!target) throw new Error("solo mode requires exactly one provider");
  const p = getProvider(target.id);
  const complete: CompletionRequest = {
    model: target.model,
    messages,
    temperature: target.temperature,
    tools,
    signal: req.signal,
  };
  for await (const evt of p.stream(complete)) {
    write({ ...evt, providerId: p.id });
  }
}

async function runParallel(
  req: HermesRequest,
  messages: ChatMessage[],
  tools: ToolSpec[],
  write: (e: TaggedEvent) => void,
): Promise<void> {
  const tasks = req.providers.map(async (t) => {
    const p = getProvider(t.id);
    const complete: CompletionRequest = {
      model: t.model,
      messages,
      temperature: t.temperature,
      tools,
      signal: req.signal,
    };
    for await (const evt of p.stream(complete)) {
      write({ ...evt, providerId: p.id });
    }
  });
  await Promise.allSettled(tasks);
}

async function runSynthesis(
  req: HermesRequest,
  messages: ChatMessage[],
  tools: ToolSpec[],
  write: (e: TaggedEvent) => void,
): Promise<void> {
  // 1. Fan-out in parallel, also buffering each provider's full text
  //    reply so we can feed it into the synthesiser.
  const texts = new Map<ProviderId, string>();
  const tasks = req.providers.map(async (t) => {
    const p = getProvider(t.id);
    const complete: CompletionRequest = {
      model: t.model,
      messages,
      temperature: t.temperature,
      tools,
      signal: req.signal,
    };
    let buf = "";
    for await (const evt of p.stream(complete)) {
      if (evt.type === "text") buf += evt.delta;
      write({ ...evt, providerId: p.id });
    }
    texts.set(p.id, buf);
  });
  await Promise.allSettled(tasks);

  // 2. Let the synthesiser reconcile. Default to the first available
  //    provider if ``synthesiser`` is unset.
  const synId = req.synthesiser ?? req.providers[0]?.id;
  if (!synId) return;
  const synTarget = req.providers.find((t) => t.id === synId);
  if (!synTarget) return;
  const p = getProvider(synTarget.id);
  const synPrompt: ChatMessage[] = [
    ...messages,
    ...[...texts.entries()].map(([pid, t]) => ({
      role: "user" as const,
      content: `[Previous from ${pid}]\n${t}`,
    })),
    {
      role: "user" as const,
      content:
        "Synthesise the above answers into one reconciled reply. " +
        "If any claim is contradicted across providers, surface the " +
        "contradiction rather than hide it. Cite MCP tools as [tool:<name>].",
    },
  ];
  for await (const evt of p.stream({
    model: synTarget.model,
    messages: synPrompt,
    temperature: 0.0,
    tools,
    signal: req.signal,
  })) {
    write({ ...evt, providerId: p.id });
  }
}

async function runRelay(
  req: HermesRequest,
  messages: ChatMessage[],
  tools: ToolSpec[],
  write: (e: TaggedEvent) => void,
): Promise<void> {
  let running: ChatMessage[] = [...messages];
  for (const target of req.providers) {
    const p = getProvider(target.id);
    let buf = "";
    for await (const evt of p.stream({
      model: target.model,
      messages: running,
      temperature: target.temperature,
      tools,
      signal: req.signal,
    })) {
      if (evt.type === "text") buf += evt.delta;
      write({ ...evt, providerId: p.id });
    }
    // Feed the reply to the next hop — prefix so downstream models
    // don't mistake the previous provider's text for their own prior
    // output (which confuses role tracking).
    running = [
      ...running,
      {
        role: "user",
        content: `[Previous from ${p.id}] ${buf}`,
        origin: p.id,
      },
    ];
  }
}

function errMsg(e: unknown): string {
  if (e instanceof Error) return `${e.name}: ${e.message}`;
  return String(e);
}

/**
 * Convenience used by ``ProviderPicker.tsx`` to know which providers
 * to show. Kept here so the UI doesn't need to import all adapter
 * modules (which pull in heavy SDKs).
 */
export function availableProviderIds(): ProviderId[] {
  return [...getProviders().values()]
    .filter((p) => p.available())
    .map((p) => p.id);
}
