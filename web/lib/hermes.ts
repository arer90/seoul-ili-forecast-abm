/**
 * Hermes — the cross-provider orchestrator. Four modes:
 *
 * - **solo**       — single provider, streams directly.
 * - **parallel**   — fan out to N providers, interleave their events
 *                    with a ``providerId`` tag so the UI can render
 *                    columns side by side.
 * - **synthesis**  — fan out in parallel, then once every provider
 *                    reaches ``done`` ask a ``synthesiser`` provider to
 *                    reconcile the answers into one.
 * - **relay**      — sequentially chain providers, feeding each reply
 *                    as a ``[Previous from X]`` prefixed user turn into
 *                    the next provider (so they can critique / extend).
 *
 * All four share a single upstream ``ReadableStream`` of ``StreamEvent``
 * objects tagged with ``providerId``. The route handler re-serialises
 * them to SSE for the browser.
 */

import { listTools } from "./mcp-client";
import { getProvider, getProviders } from "./providers/registry";
import ariaWiki from "./aria-wiki.json";
import type {
  ChatMessage,
  CompletionRequest,
  ProviderId,
  ResponseMode,
  StreamEvent,
  ToolSpec,
} from "./providers/types";

export type TaggedEvent = StreamEvent & { providerId: ProviderId };

export interface HermesRequest {
  mode: ResponseMode;
  messages: ChatMessage[];
  providers: Array<{ id: ProviderId; model: string; temperature?: number }>;
  /**
   * For ``synthesis`` mode only — which provider writes the final
   * reconciliation. Must be present in ``providers``.
   */
  synthesiser?: ProviderId;
  /** Abort from the route handler — propagated to every provider. */
  signal?: AbortSignal;
}

